package com.example.projekfirebase;

public class Barang {
    private String nomor_peg;
    private String nama_peg;
    private String jabatan;
    private String key;

    public String getNomor_peg() {
        return nomor_peg;
    }

    public void setNomor_peg(String nomor_peg) {
        this.nomor_peg = nomor_peg;
    }

    public String getNama_peg() {
        return nama_peg;
    }

    public void setNama_peg(String nama_peg) {
        this.nama_peg = nama_peg;
    }

    public String getJabatan() {
        return jabatan;
    }

    public void setJabatan(String jabatan) {
        this.jabatan = jabatan;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}


